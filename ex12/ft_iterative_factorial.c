int	ft_iterative_factorial(int nb)
{
	int i;

	i = nb - 1;
	if (nb > 0)
	{
		while (i >= 1)
		{
			nb *= i;
			i--;
		}
		return (nb);
	}
	if (nb == 0)
		return (1);
	else
		return (0);
}
