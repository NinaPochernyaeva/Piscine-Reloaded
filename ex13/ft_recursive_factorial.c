#include <stdio.h>

int	ft_recursive_factorial(int nb)
{
	if (nb == 0 || nb == 1)
		return (1);
	if (nb > 1 && nb < 14)
		return (ft_recursive_factorial(nb - 1) * nb);
	else
		return (0);
}
