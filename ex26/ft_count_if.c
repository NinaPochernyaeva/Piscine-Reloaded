int	ft_count_if(char **tab, int (*f)(char*))
{
	int i;
	int number;

	i = 0;
	number = 0;
	while (tab[i])
	{
		if ((*f)(tab[i]) == 1)
			number++;
		i++;
	}
	return (number);
}
